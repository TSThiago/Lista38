class Carro{
    Marca : string
    Modelo : string
    Categoria : string
    Ano : number
    Quilometragem : number 
    Valor : number

    constructor(marca : string , modelo : string, categoria : string, ano : number, quilometragem : number, valor : number){
        this.Marca = marca
        this.Modelo = modelo
        this.Categoria = categoria
        this.Ano = ano
        this.Quilometragem = quilometragem
        this.Valor = valor
    }
}